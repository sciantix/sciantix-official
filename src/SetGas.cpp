#include "SetGas.h"

/**
 * @brief This routines defines the gas objects
 * 
 */

void SetGas()
{
	for (int k = 0; k < 10; ++k)
	{
		switch (k)
		{
		case 0: Xenon();              MapGas();       break;
		case 1: Krypton();            MapGas();       break;
		case 2: Helium();             MapGas();       break;

		default:                                      break;
		}
	}
}
