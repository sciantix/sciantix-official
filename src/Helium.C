#include "Helium.h"

/// Helium

void Helium()
{
	gas.emplace_back();
	int index = int(gas.size()) - 1;
	gas[index].setName("He");
	gas[index].setAtomicNumber(2);
	gas[index].setVanDerWaalsVolume(9.97e-30);
	gas[index].setDecayRate(0.0);
	gas[index].setPrecursorFactor(1.00);
}
